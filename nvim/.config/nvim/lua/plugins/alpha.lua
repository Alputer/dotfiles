    ]]

    dashboard.section.header.val = vim.split(logo, "\n")
    -- stylua: ignore
    dashboard.section.buttons.val = {
      dashboard.button("n", "п…ӣ " .. " New file",        [[<cmd> enew <BAR> startinsert <cr>]]),
      dashboard.button("r",  "пғ…  Recent files",   "<cmd> Telescope oldfiles <cr>"),
      dashboard.button("f", "пҖӮ  Find files", "<cmd> Telescope find_files <cr>"),
      dashboard.button("g", "пҖў  Live grep",  "<cmd> Telescope live_grep <cr>"),
      dashboard.button("t",  "о—ҝ  File tree",      "<cmd> NvimTreeToggle <cr>"),
      dashboard.button("c", "оҳ•  Config", ":e ~/dotfiles/nvim/<CR>"),
      dashboard.button("l", "у°’І " .. " Lazy",            "<cmd> Lazy <cr>"),
      dashboard.button("q", "пҗҰ " .. " Quit",            "<cmd> qa <cr>"),
    }
    for _, button in ipairs(dashboard.section.buttons.val) do
      button.opts.hl = "AlphaButtons"
      button.opts.hl_shortcut = "AlphaShortcut"
    end
    dashboard.section.header.opts.hl = "AlphaHeader"
    dashboard.section.buttons.opts.hl = "AlphaButtons"
    dashboard.section.footer.opts.hl = "AlphaFooter"
    dashboard.opts.layout[1].val = 8
    return dashboard
  end,
  config = function(_, dashboard)
    -- close Lazy and re-open when the dashboard is ready
    if vim.o.filetype == "lazy" then
      vim.cmd.close()
      vim.api.nvim_create_autocmd("User", {
        once = true,
        pattern = "AlphaReady",
        callback = function()
          require("lazy").show()
        end,
      })
    end

    require("alpha").setup(dashboard.opts)

    vim.api.nvim_create_autocmd("User", {
      once = true,
      pattern = "LazyVimStarted",
      callback = function()
        local stats = require("lazy").stats()
        local ms = (math.floor(stats.startuptime * 100 + 0.5) / 100)
        dashboard.section.footer.val = "вҡЎ Neovim loaded "
          .. stats.loaded
          .. "/"
          .. stats.count
          .. " plugins in "
          .. ms
          .. "ms"
        pcall(vim.cmd.AlphaRedraw)
      end,
    })
  end,
}
